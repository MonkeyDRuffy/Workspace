package Rechnen;

public class rechenprogramm {

	public static void main(String[] args) {

		double zahl1 = 2.5;
		double zahl2 = 2.5;
		double summe = zahl1 + zahl2;
		double dif = zahl1 - zahl2;
		double pro = zahl1 * zahl2;
		double qui = zahl1 / zahl2;
		
		System.out.println("Ergebnis :" + summe);
		System.out.println("Differenz :" + dif);
		System.out.println("Produkt :" + pro);
		System.out.println("Qoutient :" + qui);
	}

}
